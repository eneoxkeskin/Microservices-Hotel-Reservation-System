const getRandomImage = () => {
  const randomIndex = Math.floor(Math.random() * defaultImages.length);
  return defaultImages[randomIndex];
};

const hotelSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  city: {
    type: String,
    required: true,
  },
  country: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  pricePerNight: {
    type: Number,
    required: true,
  },
  starRating: {
    type: Number,
    required: true,
    min: 1,
    max: 5,
  },
  count: {
    type: Number,
    default: 0,
  },

  defaultImage: {
    type: String,
    default: getRandomImage, 
  }
});

const Hotel = mongoose.model("Hotel", hotelSchema);

export default Hotel;
